<b>Пополнение счета на сайте через Сбербанк</b><br />
Сбербанк на сегодня является самым крупным банком в России, имея десятки тысяч филиалов по всей стране. Вы можете через сбербанк пополнить счет на сайте переводом на нашу карту любым из следующих способов:
<br /> 1. C карты на карту Сбербанка Visa или Maestro (через банкомат или интернет-банк "Сбербанк Онл@йн). Время перевода - мгновенно. Комиссия банка - 1 % (не взимается с карт, выпущенных Московским банком Сбербанка России);
<br /> 2. Через операциониста Сбербанка (в любом филиале Вы можете перевести сумму на нашу карту). Время перевода - до 24 часов. Комиссия банка - 0,75%.<br />
<b>Как оплатить через Сбербанк:</b><br />
1. На странице пополнения счета выберите Сбербанк.<br />
2. Переведите деньги на карту. (в заявке будет указана карта на которую перевести деньги).<br />
2. Заполните форму заявки на пополнение счета. Ниже будет создана новая заявка на пополнение счета.<br />
3. Добавьте отсканированную или сфотографированную квитанцию об оплате. Если вы переводили деньги через
Сбербанк ОнЛ@йн, то сделайте скриншот квитанции, которая появится после перевода.<br />
